pResidue := function(G,p)
    return NormalClosure(G, Sylow(G,p));
end function;

G := Sp(8,3);
M := MaximalSubgroups(G);
M := [A`subgroup : A in M];
M := [A : A in M | Index(A, pCore(A,3)) mod 3^5 eq 0];

M1 := [A : A in M | #A eq 2 * #GL(4,3)][1]; // $M_1 := 2 \times \GL_4(3)$
M2 := [A : A in M | #A eq 24^4 * 24][1]; // $M_2 := \SL_2(3) \wr \Sym(4)$
M3 := [A : A in M | #A eq 2 * #GU(4,3)][1]; // $M_3 := 2 \times \GU_4(3)$
M4 := [A : A in M | #A eq 2 * #Sp(4,9)][1]; // $M_4 := \Sp_4(9) : 2$
M5 := [A : A in M | #A eq #Sp(4,3)^2 * 2][1]; // $M_5 := \Sp_4(3)^2 : 2$
M6 := [A : A in M | #A eq #SL(2,3) * #Sp(6,3)][1]; // $M_6 := \SL_2(3) \times \Sp_6(3)$
M7 := [A : A in M | #A eq 3^10 * #GL(4,3)][1]; // $M_7 := 3^10 : GL_4(3)$
M8 := [A : A in M | #A eq 3^11 * 2 * #SL(2,3) * #Sp(4,3)]; // $M_8 := 3^{3+8} : (\SL_2(3) \times \Sp_4(3)) : 2$
M9 := [A : A in M | #A eq 3^7 * 2 * #Sp(6,3)][1]; // $M_9 := 2 \times 3^{1+6} : (\Sp_6(3) : 2)$

// -------------------------------------------------------------------------------------------------------------------------------------------------------------------
// $\GL_4(3)$ has no subgroups of interest
// -------------------------------------------------------------------------------------------------------------------------------------------------------------------
A := Subgroups(M1 : OrderMultipleOf := 3^5);
A := [X`subgroup : X in A];
A := [X : X in A | #pCore(X,3) eq 1 and <3,5> in FactoredOrder(X)];
#A; // should be 0

// -------------------------------------------------------------------------------------------------------------------------------------------------------------------
// $\SL_2(3) \wr \Sym(4)$ has center order $2$
// -------------------------------------------------------------------------------------------------------------------------------------------------------------------
#Center(M2); // should be 2

// -------------------------------------------------------------------------------------------------------------------------------------------------------------------
// $\SU_4(3)$ has no subgroups of interest
// -------------------------------------------------------------------------------------------------------------------------------------------------------------------
A := Subgroups(pResidue(M3,3) : OrderMultipleOf := 3^5);
A := [X`subgroup : X in A];
A := [X : X in A | #pCore(X,3) eq 1 and <3,5> in FactoredOrder(X)];
#A; // should be 0

// -------------------------------------------------------------------------------------------------------------------------------------------------------------------
// $\SU_4(3)$ has no subgroups of interest
// -------------------------------------------------------------------------------------------------------------------------------------------------------------------
A := Subgroups(pResidue(M4,3) : OrderMultipleOf := 3^5);
A := [X`subgroup : X in A];
A := [X : X in A | #pCore(X,3) eq 1 and <3,5> in FactoredOrder(X)];
#A; // should be 0

// -------------------------------------------------------------------------------------------------------------------------------------------------------------------
// $\Sp_4(3)^2$ only contributes $\SL_2(3) \times \Sp_4(3)$
// -------------------------------------------------------------------------------------------------------------------------------------------------------------------
Max := MaximalSubgroups(pResidue(M5,3));
Max := [A`subgroup : A in Max];
Max := [A : A in Max | Index(A,pCore(A,3)) mod 3^5 eq 0];
Max3 := [A/pCore(A,3) : A in Max];


